class CicloFor {
    public static void main(String[] args) {
        int i;

        for (i = 1; i < 101; i++) {
            System.out.print(i + " ");
        }

        System.out.println(" ");

        for (i = 250; i < 1201; i++) {
            System.out.print(i + "  ");
        }

        System.out.println(" ");

        
    }
}