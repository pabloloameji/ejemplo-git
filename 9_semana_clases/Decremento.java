public class Decremento {
    public static void main(String[] args) {
        int cont = 5;

        --cont;
        cont--;

        System.out.println(cont);
        System.out.println(--cont);
        System.out.println(cont--);

        --cont;

        System.out.println(cont--);
        System.out.println(--cont);
        System.out.println(cont);
    }
}
